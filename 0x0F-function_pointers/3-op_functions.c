#include "3-calc.h"

/**
  * op_add - a function calculates sum two numbers
  * @a: first number
  * @b: second number
  * Return: the sum of the two numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - a function calculates sub two numbers
  * @a: first number
  * @b: second number
  * Return: the sub of two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function calculates multipy two numbers
 * @a: first number
 * @b: second number
 * Return: the multplication of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_mod - a function calculates modulo two numbers
  * @a: first number
  * @b: second number
  * Return: the division of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - a function calculates divistion two numbers
 * @a: first number
 * @b: second number
 * Return: the division of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
